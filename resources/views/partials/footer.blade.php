<script>
    //statis
    document.addEventListener('DOMContentLoaded', function() {
     
        const counters = document.querySelectorAll('.stat-number');

        const animateCounter = (counter) => {
            const target = parseInt(counter.getAttribute('data-count'));
            const suffix = counter.textContent.includes('%') ? '%' : '';
            const duration = 2000;
            const step = target / (duration / 16);
            let current = 0;

            const updateCounter = () => {
                current += step;
                if (current < target) {
                    counter.textContent = suffix === '%' ?
                        Math.round(current * 10) / 10 + suffix :
                        Math.round(current).toLocaleString();
                    requestAnimationFrame(updateCounter);
                } else {
                    counter.textContent = suffix === '%' ?
                        target.toFixed(1) + suffix :
                        target.toLocaleString();
                }
            };

            updateCounter();
        };

        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateCounter(entry.target);
                    observer.unobserve(entry.target);
                }
            });
        }, {
            threshold: 0.5
        });

        counters.forEach(counter => observer.observe(counter));

        // Newsletter Form
        const footerNewsletterForm = document.getElementById('footerNewsletterForm');
        if (footerNewsletterForm) {
            footerNewsletterForm.addEventListener('submit', async function(e) {
                e.preventDefault();

                const form = this;
                const submitBtn = form.querySelector('.footer-newsletter-btn');
                const messageDiv = form.querySelector('.newsletter-message');
                const originalHTML = submitBtn.innerHTML;

                
                submitBtn.innerHTML = `
                <svg width="20" height="20" viewBox="0 0 20 20" fill="none" class="loading-spinner">
                    <path d="M10 2C14.4183 2 18 5.58172 18 10" stroke="currentColor" stroke-width="2"/>
                </svg>
            `;

                try {
                    const response = await fetch(form.dataset.url, {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                            'Accept': 'application/json',
                            'X-CSRF-TOKEN': document.querySelector(
                                'meta[name="csrf-token"]').content
                        },
                        body: JSON.stringify({
                            email: form.querySelector('#footerNewsletterEmail')
                                .value,
                            source: 'footer'
                        })
                    });

                    const data = await response.json();

                    if (data.success) {
                        messageDiv.style.display = 'block';
                        messageDiv.innerHTML = `
                        <div style="color: #4CAF50; background: rgba(76, 175, 80, 0.1); padding: 12px; border-radius: 8px;">
                            ${data.message}
                        </div>
                    `;
                        form.reset();

                        // Analytics event
                        if (typeof gtag !== 'undefined') {
                            gtag('event', 'newsletter_subscription', {
                                'event_category': 'engagement',
                                'event_label': 'footer'
                            });
                        }
                    } else {
                        messageDiv.style.display = 'block';
                        messageDiv.innerHTML = `
                        <div style="color: #ff6b6b; background: rgba(255, 107, 107, 0.1); padding: 12px; border-radius: 8px;">
                            ${data.message}
                        </div>
                    `;
                    }
                } catch (error) {
                    console.error('Error:', error);
                    messageDiv.style.display = 'block';
                    messageDiv.innerHTML = `
                    <div style="color: #ff6b6b; background: rgba(255, 107, 107, 0.1); padding: 12px; border-radius: 8px;">
                        Erreur de connexion. Veuillez réessayer.
                    </div>
                `;
                } finally {
                    submitBtn.innerHTML = originalHTML;

                    
                    setTimeout(() => {
                        messageDiv.style.display = 'none';
                    }, 5000);
                }
            });
        }

      
        const langToggle = document.getElementById('langToggle');
        const langDropdown = document.getElementById('langDropdown');

        if (langToggle && langDropdown) {
            langToggle.addEventListener('click', function(e) {
                e.stopPropagation();
                langDropdown.classList.toggle('show');
            });

          
            document.addEventListener('click', function() {
                langDropdown.classList.remove('show');
            });

            
            langDropdown.querySelectorAll('.lang-option').forEach(option => {
                option.addEventListener('click', function(e) {
                    e.preventDefault();
                    const lang = this.textContent;
                    langToggle.querySelector('span').textContent = lang;
                    langDropdown.classList.remove('show');

               
                    console.log('Language changed to:', lang);
                });
            });
        }

        // Back to Top Button
        const backToTopBtn = document.getElementById('backToTop');

        if (backToTopBtn) {
            window.addEventListener('scroll', function() {
                if (window.pageYOffset > 300) {
                    backToTopBtn.classList.add('visible');
                } else {
                    backToTopBtn.classList.remove('visible');
                }
            });

            backToTopBtn.addEventListener('click', function() {
                window.scrollTo({
                    top: 0,
                    behavior: 'smooth'
                });
            });
        }

    
        window.addEventListener('scroll', function() {
            const scrollPosition = window.pageYOffset;
            const circles = document.querySelectorAll('.bg-circle');

            circles.forEach((circle, index) => {
                const speed = 0.5 + (index * 0.1);
                const yPos = -(scrollPosition * speed * 0.1);
                circle.style.transform = `translateY(${yPos}px)`;
            });
        });

